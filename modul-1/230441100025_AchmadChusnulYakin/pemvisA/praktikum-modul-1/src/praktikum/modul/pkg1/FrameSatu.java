    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
    // ActionListener untuk Button Simpan
        Submit.addActionListener(new java.awt.event.ActionListener() {
    public void actionPerformed(java.awt.event.ActionEvent evt) {
        // Mengecek apakah checkbox "Setuju Syarat dan Ketentuan" sudah dicentang
        if (!SyaratDanKetentuan.isSelected()) {
            // Tampilkan pesan peringatan jika checkbox belum dicentang
            JOptionPane.showMessageDialog(null, "Anda harus menyetujui syarat dan ketentuan sebelum menyimpan!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            
        // Ambil data dari field di frame pertama
        String nama = NamaPasien.getText();
        String NIK = NIKPasien.getText();
        String alamat = AlamatPasien.getText();
        String telepon = NoTeleponPasien.getText();
        String email = EmailPasien.getText();
        String jenisKelamin = Pria.isSelected() ? "Laki-Laki" : "Perempuan";
        String kategoriLayanan = KategoriLayanan.getSelectedItem().toString();
        String tanggalKunjungan = TanggalKunjungan.getText();

        // Ambil status checkbox (Setuju/Tidak Setuju)
        String syaratKetentuan = SyaratDanKetentuan.isSelected() ? "Setuju" : "Tidak Setuju";
        
        // Jika checkbox sudah dicentang, maka lanjutkan menyimpan data dan membuka frame baru
        FrameDua frameDua = new FrameDua(nama, NIK, alamat, telepon, email, tanggalKunjungan, jenisKelamin, kategoriLayanan, syaratKetentuan);
        frameDua.setVisible(true);
            
            // Menyembunyikan frame yang sedang aktif (opsional)
//            dispose();
        }
    }
});
    }//GEN-LAST:event_SubmitActionPerformed

