    private void KirimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_KirimActionPerformed
        // TODO add your handling code here:
                if(nama.getText().isEmpty() && jmlspp.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Isi Dulu Bro", "Warning!", JOptionPane.WARNING_MESSAGE);
        }
        if(!januari.isSelected() && !februari.isSelected() && !maret.isSelected() && !april.isSelected() 
                && !mei.isSelected() &&!juni.isSelected() && !juli.isSelected() && !agustus.isSelected() 
                && !september.isSelected() && !oktober.isSelected() && !november.isSelected() && !desember.isSelected()) {
            JOptionPane.showMessageDialog(this, "Pilih dulu mau bayar yang mana", "Warning!", JOptionPane.WARNING_MESSAGE);
        }
        try{
            // Mengambil model tabel untuk menambahkan baris data baru
            DefaultTableModel untuktable = (DefaultTableModel) jTable1.getModel();
            String name = nama.getText();// Mengambil nama dari input
            String Status = "Nunggak";// Status default jika belum lunas
            int utang = 0;
            int sppbulannya = Integer.parseInt(jmlspp.getText());// Konversi jumlah SPP dari teks ke angka
            
            int untuksemuabulan = 0;// Variabel untuk menghitung bulan yang dipilih
        // Cek apakah bulan Januari dipilih  
        if (januari.isSelected()) {
            untuksemuabulan++;
        }
        
        if (februari.isSelected()) {
            untuksemuabulan++;
        }
        
        if (maret.isSelected()) {
            untuksemuabulan++;
        }
        
        if (april.isSelected()){
            untuksemuabulan++;
        }
        
        if (mei.isSelected()){
            untuksemuabulan++;
        }
        
        if (juni.isSelected()){
            untuksemuabulan++;
        }
        if (juli.isSelected()){
            untuksemuabulan++;
        }
        if (agustus.isSelected()){
            untuksemuabulan++;
        }
        
        if (september.isSelected()){
            untuksemuabulan++;
        }
        if (oktober.isSelected()){
            untuksemuabulan++;
        }
        if (november.isSelected()){
            untuksemuabulan++;
        }
        if (desember.isSelected()){
            untuksemuabulan++;
        }
        // Jika semua bulan dipilih (12 bulan), set status jadi "lunas"
        if (untuksemuabulan == 12) {
            Status = "lunas";
        }
         // Hitung jumlah utang yang tersisa
        utang = sppbulannya * (12 - untuksemuabulan);
         // Membuat array untuk menampung data baru yang akan ditambahkan ke tabel
        Object[] rowData = new Object[15];
        rowData[0] = name;
        rowData[1] = Status;
        rowData[2] = utang;
        rowData[3] = januari.isSelected();
        rowData[4] = februari.isSelected();
        rowData[5] = maret.isSelected();
        rowData[6] = april.isSelected();
        rowData[7] = mei.isSelected();
        rowData[8] = juni.isSelected();
        rowData[9] = juli.isSelected();
        rowData[10] = agustus.isSelected();
        rowData[11] = september.isSelected();
        rowData[12] = oktober.isSelected();
        rowData[13] = november.isSelected();
        rowData[14] = desember.isSelected();
        // Menambahkan data baru ke tabel
        untuktable.addRow(rowData);
         // Reset input setelah data ditambahkan
        nama.setText("");
        jmlspp.setText("");
        januari.setSelected(false);
        februari.setSelected(false);
        maret.setSelected(false);
        april.setSelected(false);
        mei.setSelected(false);
        juni.setSelected(false);
        juli.setSelected(false);
        agustus.setSelected(false);
        september.setSelected(false);
        oktober.setSelected(false);
        november.setSelected(false);
        desember.setSelected(false);
           // Menampilkan pesan sukses  
        JOptionPane.showMessageDialog(this, "Data Berhasil Ditambah", "Message", JOptionPane.WARNING_MESSAGE);
        }catch (NumberFormatException e){
             // Menangani kesalahan jika input jumlah SPP bukan angka
            JOptionPane.showMessageDialog(this, "Masukkan SPP Dalam bentuk angka", "Warning!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_KirimActionPerformed

