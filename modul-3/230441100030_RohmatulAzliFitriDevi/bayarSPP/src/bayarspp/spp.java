    private void jTextFieldcashKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldcashKeyReleased
        // TODO add your handling code here:
      
        int cash = Integer.parseInt(jTextFieldcash.getText());
        double jumlahtotal = Double.parseDouble(jTextFieldtotal.getText());
        double kembali = cash - jumlahtotal;

        jTextFieldkembalian.setText(String.valueOf(kembali));
        jTextFieldkembalian.setEditable(false);
    
    }//GEN-LAST:event_jTextFieldcashKeyReleased

    private void jButtonbayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonbayarActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();

        if (!jTextFieldnama.getText().isEmpty() && !jTextFieldjumlahspp.getText().isEmpty() && !jTextFieldcash.getText().isEmpty()) {

            int total = Integer.parseInt(jTextFieldtotal.getText());
            int cash = Integer.parseInt(jTextFieldcash.getText());

            if (cash >= total) {
                int kembalian = cash - total;
                jTextFieldkembalian.setText(Integer.toString(kembalian));

                StringBuilder lunas = new StringBuilder();
                StringBuilder menunggak = new StringBuilder();

                String[] namaBulan = {"Jan", "Feb", "Mar", "Apr", "Mei", "Jun", "Jul", "Agust", "Sept", "Okt", "Nov", "Des"};
                for (int i = 0; i < checkBoxesBulan.length; i++) {
                    if (checkBoxesBulan[i].isSelected()) {
                        lunas.append(namaBulan[i]).append(", ");
                    } else {
                        menunggak.append(namaBulan[i]).append(", ");
                    }
                }

                if (lunas.length() > 0) {
                    lunas.setLength(lunas.length() - 2);  
                }
                if (menunggak.length() > 0) {
                    menunggak.setLength(menunggak.length() - 2);  
                }

                JOptionPane.showMessageDialog(this, "Pembayaran berhasil!", "Informasi", JOptionPane.INFORMATION_MESSAGE);

                Object[] row = {jTextFieldnama.getText(), jTextFieldtotal.getText(), lunas.toString(), menunggak.toString()};
                model.insertRow(0, row);  

            } else {
                JOptionPane.showMessageDialog(this, "Pembayaran gagal, uang anda tidak cukup", "Peringatan", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Harap lengkapi semua data!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_jButtonbayarActionPerformed

