    private void BAYARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAYARActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tabel = (DefaultTableModel) tabelspp.getModel();
//        tabel.setRowCount(0);

        if (!nama.getText().isEmpty() && !textfieldjumlah.getText().isEmpty() && !cash.getText().isEmpty()) {

        int total = Integer.parseInt(textfieldtotal.getText());
        int uangcash = Integer.parseInt(cash.getText());

        if (uangcash >= total) {
                
        int kembali = uangcash - total;
        kembalian.setText(Integer.toString(kembali));

        StringBuilder lunasBulan = new StringBuilder();
        StringBuilder menunggakBulan = new StringBuilder();

        String[] namaBulan = {"Januari", "Februari", "Maret", "April", "Mei", "Juni", 
            "Juli", "Agustus", "September", "Oktober", "November", "Desember"};
            
        for (int i = 0; i < checkBoxesBulan.length; i++) {
            if (checkBoxesBulan[i].isSelected()) {
                lunasBulan.append(namaBulan[i]).append(", ");
            } else {
                menunggakBulan.append(namaBulan[i]).append(", ");
                    }
                }
        
        if (lunasBulan.length() > 0) {
            lunasBulan.setLength(lunasBulan.length() - 2);  
        } if (menunggakBulan.length() > 0) {
            menunggakBulan.setLength(menunggakBulan.length() - 2);  // untuk ngehapus koma dan spasi
        }
        JOptionPane.showMessageDialog(this, "Pembayaran berhasil!", "Informasi", JOptionPane.INFORMATION_MESSAGE);

        Object[] row = {nama.getText(), textfieldtotal.getText(), lunasBulan.toString(), menunggakBulan.toString()};
        tabel.insertRow(0, row);  

                
        cash.setText("");
        kembalian.setText("");
        textfieldtotal.setText("");
        nama.setText("");  
        textfieldjumlah.setText(""); 
        
        } else {
            JOptionPane.showMessageDialog(this, "Pembayaran gagal, uang anda tidak cukup", "Peringatan", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Harap lengkapi semua data!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_BAYARActionPerformed

