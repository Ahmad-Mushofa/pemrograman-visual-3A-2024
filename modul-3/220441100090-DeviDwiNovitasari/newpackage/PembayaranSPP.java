    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here:
        if (januari.isSelected()) pembayaranSPP[0] = true;
        if (februari.isSelected()) pembayaranSPP[1] = true;
        if (maret.isSelected()) pembayaranSPP[2] = true;
        if (april.isSelected()) pembayaranSPP[3] = true;
        if (mei.isSelected()) pembayaranSPP[4] = true;
        if (juni.isSelected()) pembayaranSPP[5] = true;
        if (juli.isSelected()) pembayaranSPP[6] = true;
        if (agustus.isSelected()) pembayaranSPP[7] = true;
        if (september.isSelected()) pembayaranSPP[8] = true;
        if (oktober.isSelected()) pembayaranSPP[9] = true;
        if (november.isSelected()) pembayaranSPP[10] = true;
        if (desember.isSelected()) pembayaranSPP[11] = true;
        
        JOptionPane.showMessageDialog(this, "Pembayaran Berhasil", "information", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_btnBayarActionPerformed

    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
        int bayarSppPerBulan = Integer.parseInt(uangSPP.getText());
        int totalBayar = 0;
        
        if (januari.isSelected() && !pembayaranSPP[0]) totalBayar += bayarSppPerBulan;
        if (februari.isSelected() && !pembayaranSPP[1]) totalBayar += bayarSppPerBulan;
        if (maret.isSelected() && !pembayaranSPP[2]) totalBayar += bayarSppPerBulan;
        if (april.isSelected() && !pembayaranSPP[3]) totalBayar += bayarSppPerBulan;
        if (mei.isSelected() && !pembayaranSPP[4]) totalBayar += bayarSppPerBulan;
        if (juni.isSelected() && !pembayaranSPP[5]) totalBayar += bayarSppPerBulan;
        if (juli.isSelected() && !pembayaranSPP[6]) totalBayar += bayarSppPerBulan;
        if (agustus.isSelected() && !pembayaranSPP[7]) totalBayar += bayarSppPerBulan;
        if (september.isSelected() && !pembayaranSPP[8]) totalBayar += bayarSppPerBulan;
        if (oktober.isSelected() && !pembayaranSPP[9]) totalBayar += bayarSppPerBulan;
        if (november.isSelected() && !pembayaranSPP[10]) totalBayar += bayarSppPerBulan;
        if (desember.isSelected() && !pembayaranSPP[11]) totalBayar += bayarSppPerBulan;
        
        totalSPP.setText(String.valueOf(totalBayar));
        totalSPP.setEditable(false);
    }//GEN-LAST:event_btnHitungActionPerformed

    private void btnRiwayatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRiwayatActionPerformed
        // TODO add your handling code here:
        String nama = textNama.getText();
        if (nama.isEmpty()){
            JOptionPane.showMessageDialog(this, "Harap isi Nama terlebih dahulu!!", "peringatan", JOptionPane.INFORMATION_MESSAGE);
        }
        pembayaranSPP[0] = januari.isSelected();
        pembayaranSPP[1] = februari.isSelected();
        pembayaranSPP[2] = maret.isSelected();
        pembayaranSPP[3] = april.isSelected();
        pembayaranSPP[4] = mei.isSelected();
        pembayaranSPP[5] = juni.isSelected();
        pembayaranSPP[6] = juli.isSelected();
        pembayaranSPP[7] = agustus.isSelected();
        pembayaranSPP[8] = september.isSelected();
        pembayaranSPP[9] = oktober.isSelected();
        pembayaranSPP[10] = november.isSelected();
        pembayaranSPP[11] = desember.isSelected();
        
        RiwayatPembayaran riwayat = new RiwayatPembayaran(pembayaranSPP,nama);
        riwayat.setVisible(true);
    }//GEN-LAST:event_btnRiwayatActionPerformed

