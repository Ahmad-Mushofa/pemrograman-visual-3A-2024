    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(!bodrex.isSelected() && !antimo.isSelected() && !herbal44.isSelected() && !mixagrip.isSelected() 
                && !panadol.isSelected() && !ponstan.isSelected() && !sumagesic.isSelected() && !promag.isSelected() 
                && !vitacimin.isSelected() && !tolakangin.isSelected()){
            JOptionPane.showMessageDialog(this, "Anda belum memilih Obat", "Peringatan", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
         String selectedOptions = "";
         if (bodrex.isSelected()){
             selectedOptions += bodrex.getText() + "telah dipilih\n";
         }
         if (sumagesic.isSelected()){
             selectedOptions += "Anda memilih Obat Sumagesic\n";
         }
         if (panadol.isSelected()){
             selectedOptions += "Anda memilih Obat Panadol\n";
         }
         if (herbal44.isSelected()){
             selectedOptions += "Anda memilih Sirup Herbal 44\n";
         }
         if (mixagrip.isSelected()){
             selectedOptions += "Anda memilih Obat Mixagrip FLU\n";
         }
         if (promag.isSelected()){
             selectedOptions += "Anda memilih Obat Promag\n";
         }
         if (ponstan.isSelected()){
             selectedOptions += "Anda memilih Obat Ponstan\n";
         }
         if (tolakangin.isSelected()){
             selectedOptions += "Anda memilih Tolak Angin\n";
         }
         if (vitacimin.isSelected()){
             selectedOptions += "Anda memilih Vitacimin\n";
         }
         if (antimo.isSelected()){
             selectedOptions += "Anda memilih Obat Antimo\n";
         }
         
         textBodrex.setEditable(false);
         textSumagesic.setEditable(false);
         textPanadol.setEditable(false);
         textHerbal.setEditable(false);
         textMixagrip.setEditable(false);
         textPromag.setEditable(false);
         textPonstan.setEditable(false);
         textTolakAngin.setEditable(false);
         textVitacimin.setEditable(false);
         textAntimo.setEditable(false);
         
         Apotek2 hlmn = new Apotek2(selectedOptions);
         hlmn.setVisible(true);
    }//GEN-LAST:event_btnSimpanActionPerformed
    
   
