    private void BeliObatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BeliObatActionPerformed
        // TODO add your handling code here:
        
        int PilihGejalaIndex = PilihGejala.getSelectedIndex();
        
        if (totalBelanja > 0 && (uangBayar < totalBelanja || uangKembalian < 0)) {
        JOptionPane.showMessageDialog(null, "Selesaikan pembayaran sebelumnya terlebih dahulu!", "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
        return;
    }
    
    if (buttonGroup1.getSelection() == null){
        JOptionPane.showMessageDialog(null, "Pilih Obat dulu!", "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
        return;
    }
    if (PilihGejalaIndex == 0 && bodrex.isSelected()){
        succes("bodrex", 0);
    }else if (PilihGejalaIndex == 1 && betadine.isSelected()){
        succes("betadine", 1);
    }else if(PilihGejalaIndex == 2 && diapet.isSelected()){
        succes("diapet", 2);
    }else if(PilihGejalaIndex == 3 && ibuProfen.isSelected()){
        succes("ibuProfen", 3);
    }else if(PilihGejalaIndex == 4 && insto.isSelected()){
        succes("insto", 4);
    }else if(PilihGejalaIndex == 5 && komix.isSelected()){
        succes("komix", 5);
    }else if(PilihGejalaIndex == 6 && mixagrip.isSelected()){
        succes("mixagrip", 6);
    }else if(PilihGejalaIndex == 7 && obhCombi.isSelected()){
        succes("obhCombi", 7);
    }else if(PilihGejalaIndex == 8 && paracetamol.isSelected()){
        succes("paracetamol", 8);
    }else if(PilihGejalaIndex == 9 && promag.isSelected()){
        succes("promag", 9);
    }else{
        JOptionPane.showMessageDialog(null, "Pembelian Gagal Karena Tidak Cocok Dengan Penyakit", "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
    }

    }//GEN-LAST:event_BeliObatActionPerformed

