    private void bayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bayarActionPerformed
        // TODO add your handling code here:
    uangBayar = Double.parseDouble(cash.getText());
    if (uangBayar < totalBelanja) {
        JOptionPane.showMessageDialog(null, "Uang Pembayaran Kurang!", "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
        return;
    }
    uangKembalian = uangBayar - totalBelanja;
    kembalian.setText(String.format("%.2f", uangKembalian));
    JOptionPane.showMessageDialog(null, "Terimakasih Telah Berbelanja!", "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
    totalBelanja = 0;
    total.setText(String.format("%.2f", totalBelanja));
    cash.setText("");
    kembalian.setText("");
    }//GEN-LAST:event_bayarActionPerformed

