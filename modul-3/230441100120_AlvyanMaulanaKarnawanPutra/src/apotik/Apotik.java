    private void rSMaterialButtonRectangle13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonRectangle13ActionPerformed
        // TODO add your handling code here:
        int pilih = gejala.getSelectedIndex();
    
        if (pilih == 0){
            JOptionPane.showMessageDialog(this,"TOLONG PILIH KATEGORI TERLEBIH DAHULU", "WARNING", JOptionPane.WARNING_MESSAGE);
        }else if (pilih == 1){
            JOptionPane.showMessageDialog(this,"Super Tetra \nuntuk Mencegah Infeksi ", "REKOMENDASI OBAT", JOptionPane.INFORMATION_MESSAGE);
        }else if (pilih == 2){
            JOptionPane.showMessageDialog(this,"Komix atau Konidin \nuntuk meredakan batuk, masuk angin dan menghangatkan tubuh", "REKOMENDASI OBAT", JOptionPane.INFORMATION_MESSAGE);
        }else if (pilih == 3){
            JOptionPane.showMessageDialog(this,"Promag atau vomitrol\nuntuk meredahkan sakit lambung", "REKOMENDASI OBAT", JOptionPane.INFORMATION_MESSAGE);
        }else if (pilih == 4){
            JOptionPane.showMessageDialog(this,"Vitacimin\nuntuk menambah vitamin C", "REKOMENDASI OBAT", JOptionPane.INFORMATION_MESSAGE);
        }else if (pilih == 5){
            JOptionPane.showMessageDialog(this,"Microlax\nuntuk memperlancar buang air besar", "REKOMENDASI OBAT", JOptionPane.INFORMATION_MESSAGE);
        }else if (pilih == 6){
            JOptionPane.showMessageDialog(this,"Antanngin\nuntuk menghangatkan tubuh dan meredahkan masuk angin", "REKOMENDASI OBAT", JOptionPane.INFORMATION_MESSAGE);
        }else if (pilih == 7){
            JOptionPane.showMessageDialog(this,"Oskadon\nuntuk megurangi nyeri kepala atau pusing", "REKOMENDASI OBAT", JOptionPane.INFORMATION_MESSAGE);
        }else if (pilih == 8){
            JOptionPane.showMessageDialog(this,"paracitamol\nuntuk menurunkan panas", "REKOMENDASI OBAT", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_rSMaterialButtonRectangle13ActionPerformed

