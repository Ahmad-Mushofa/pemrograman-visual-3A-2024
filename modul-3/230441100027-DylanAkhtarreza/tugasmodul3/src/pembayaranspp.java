    private void buttonBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBayarActionPerformed
        int hasil = JOptionPane.showConfirmDialog(null, "Apakah anda ingin melanjutkan pembayaran");
        if(hasil == JOptionPane.NO_OPTION){
        JOptionPane.showMessageDialog(this, "Uang Anda Kurang","INFO", JOptionPane.WARNING_MESSAGE);
        System.exit(0);
        }
//        membuat objek dan menambahkan baris di tabel
        DefaultTableModel iniTable = (DefaultTableModel) tabelSpp.getModel();
        if(hasil == JOptionPane.YES_OPTION){
        iniTable.setRowCount(0);
//        mendeklarasikan array 
        JCheckBox [] chekbok = {CBjanuari, CBfebruari,CBmaret,CBapril,CBmei,CBjuni,CBjuli,CBagustus,CBseptember,CBoktober,CBnovember,CBdesember };
        Object[] rowData = new Object[13];

        String nama = Nama.getText();
        rowData[0] = nama;
//        String nim = Nim.getText();

        int perulangan = 0;
        int total = 0;
        for (JCheckBox inibox : chekbok){
            perulangan+=1;
            if(inibox.isSelected() == true){
                rowData[perulangan] = "lunas";
                total+=1;
            }else{
                rowData[perulangan] = "nunggak";
            }
        }
        
        iniTable.addRow(rowData);
        int totalAkhir = total*500000;
        
        
        totalTagihan.setText(String.valueOf(totalAkhir));
        JOptionPane.showMessageDialog(this, "Pembayaran Berhasil Ditambahkan","INFO", JOptionPane.WARNING_MESSAGE,ceklis);
        }
        
        Nama.setText("");
        totalTagihan.setText("");
        CBjanuari.setSelected(false);
        CBfebruari.setSelected(false);
        CBmaret.setSelected(false);
        CBapril.setSelected(false);
        CBmei.setSelected(false);
        CBjuni.setSelected(false);
        CBjuli.setSelected(false);
        CBagustus.setSelected(false);
        CBseptember.setSelected(false);
        CBoktober.setSelected(false);
        CBnovember.setSelected(false);
        CBdesember.setSelected(false);
    }//GEN-LAST:event_buttonBayarActionPerformed

