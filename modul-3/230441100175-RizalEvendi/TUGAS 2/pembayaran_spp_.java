    private void jButtonbayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonbayarActionPerformed
        String uangPembayaranStr = jTextField5.getText();

    try {
        int uangPembayaran = Integer.parseInt(uangPembayaranStr);

        if (uangPembayaran < total) {
            JOptionPane.showMessageDialog(this, "Uang tidak cukup. Total pembayaran: " + total);
            return;
        }

        int kembalian = uangPembayaran - total;

        jTextField3.setText(String.valueOf(kembalian));

        JOptionPane.showMessageDialog(this, "Pembayaran berhasil!");

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new Object[]{jTextField1.getText(), jTextField2.getText(), 
            (jRadioButton1.isSelected() ? "Laki-laki" : "Perempuan"), jComboBox1.getSelectedItem().toString(), 
            jComboBox2.getSelectedItem().toString(), selectedMonths.toString(), total, "Lunas"});


        resetForm();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Masukkan jumlah uang yang valid.");
    }


    }//GEN-LAST:event_jButtonbayarActionPerformed

