    private void jButtontotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtontotalActionPerformed
        // TODO add your handling code here:
        double hargaMenu = 0;
        if (jRadioButtonSayam.isSelected()) {
            hargaMenu = 10000;
        } else if (jRadioButtonSbabat.isSelected()) {
            hargaMenu = 12000;
        } else if (jRadioButtonSdaging.isSelected()) {
            hargaMenu = 15000;
        } else if (jRadioButtonnsori.isSelected()) {
            hargaMenu = 10000;
        } else if (jRadioButtonnssosis.isSelected()) {
            hargaMenu = 12000;
        } else if (jRadioButtonnsudang.isSelected()) {
            hargaMenu = 15000;
        }else{
            JOptionPane.showMessageDialog(null, "Yakali ga pilih menu!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        double hargaTopping = 0;
        if (jRadioButtonayam.isSelected()) {
            hargaTopping = 5000;
        } else if (jRadioButtondaging.isSelected()) {
            hargaTopping = 8000;
        } else if (jRadioButtonudang.isSelected()) {
            hargaTopping = 6000;
        }else{
            JOptionPane.showMessageDialog(null, "Yakali ga pilih menu!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int jumlah = Integer.parseInt(jTextFieldjumlah.getText());
//        mengonversinya dari teks (tipe data String) menjadi bilangan bulat
        double totalHarga = (hargaMenu + hargaTopping) * jumlah;
        if (totalHarga > 100000) {
            totalHarga = totalHarga * 0.95;
        }
        jTextFieldtotal.setText(String.valueOf(totalHarga));
        jTextFieldtotal.setEditable(false);
    }//GEN-LAST:event_jButtontotalActionPerformed

