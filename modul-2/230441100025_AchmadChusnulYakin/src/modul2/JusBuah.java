    private void ButtonTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonTotalActionPerformed

        if(JumlahPesanan.getText().equals("0")){
             JOptionPane.showMessageDialog(null,  "Mohon masukan jumlah jus yang ingin dipesan no!");
        } else if (cash.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Mohon masukan jumlah jus yang ingin dipesan ya!");
        }
        
        if (!JusApel.isSelected() && !JusJambu.isSelected() && !JusTomat.isSelected() && !JusMangga.isSelected() && !JusMangga.isSelected() && !JusStrawberry.isSelected() &&
        !JusJeruk.isSelected() && !JusAlpukat.isSelected() && !JusBuahNaga.isSelected()) {
        JOptionPane.showMessageDialog(this, "Silakan pilih jenis roti terlebih dahulu!");
        return;
        }
        
        if (!ExtraCokelatSerut.isSelected() && !ExtraWhippedCream.isSelected() && !ExtraJelly.isSelected() && !ExtraBijiSelasih.isSelected() && !ExtraKeju.isSelected() && !ExtraCeres.isSelected()) {
        JOptionPane.showMessageDialog(this, "Silakan pilih topping terlebih dahulu!");
        return;
        }
        
        if(JusApel.isSelected()){
            hargaJus = 12000;
        }else if(JusMangga.isSelected()){
            hargaJus = 12000;
        }else if(JusJambu.isSelected()){
            hargaJus = 12000;
        }else if(JusStrawberry.isSelected()){
            hargaJus = 15000;
        }else if(JusAlpukat.isSelected()){
            hargaJus = 15000;
        }else if(JusBuahNaga.isSelected()){
            hargaJus = 15000;
        }else if(JusJeruk.isSelected()){
            hargaJus = 10000;
        }else if(JusWortel.isSelected()){
            hargaJus = 10000;
        }else if(JusTomat.isSelected()){
            hargaJus = 10000;
        }
        
        if(ExtraJelly.isSelected()){
            hargaTopping = 2000;
        }else if(ExtraBijiSelasih.isSelected()){
            hargaTopping = 2000;
        }else if(ExtraCeres.isSelected()){
            hargaTopping = 2000;
        }else if(ExtraKeju.isSelected()){
            hargaTopping = 3000;
        }else if(ExtraCokelatSerut.isSelected()){
            hargaTopping = 3000;
        }else if(ExtraWhippedCream.isSelected()){
            hargaTopping = 3000;
        }
        
        double totalHarga = (hargaJus + hargaTopping)*jumlahPesanan;
                
        String infoDiskon = "";
        if(totalHarga > 100000){
            totalHarga*=0.95;
            infoDiskon = "Anda mendapatkan diskon 5%.";
        }
        
        // total.setText(String.format("Rp","%.2f",totalHarga));
        total.setText(String.valueOf(totalHarga));
        total.setEditable(false);
        
        // Validasi input uang
        if (cash.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan uang terlebih dahulu!");
            return;
        }

        // Menghitung kembalian
        try {
            double TotalHarga = Double.parseDouble(total.getText());
            double uangPembeli = Double.parseDouble(cash.getText());
            double kembalian = uangPembeli - TotalHarga;

            // Menampilkan kembalian dengan 2 angka di belakang koma
            kembali.setText(String.format("Rp %.2f", kembalian));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Masukkan jumlah uang yang valid!");
        }
        
        // Menambahkan rincian pesanan
        Rincian.setText(
            "Harga Jus          : " + hargaJus          + "\n" +
            "Harga Topping      : " + hargaTopping      + "\n" +
            "Jumlah             : " + jumlahPesanan     + "\n" +
            "Total Harga        : " + totalHarga        + "\n" +
            infoDiskon
            );
    }//GEN-LAST:event_ButtonTotalActionPerformed

