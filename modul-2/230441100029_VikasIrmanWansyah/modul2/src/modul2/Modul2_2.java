    private void cashKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cashKeyReleased
        // TODO add your handling code here:
        try{
        int HargaTotal = Integer.parseInt(totalHarga.getText());
        int Cash = Integer.parseInt(cash.getText());
        
            if (Cash < HargaTotal){
            kembalian.setText("Uang anda kurang");
            } else {
                 int Kembalian = Cash - HargaTotal;
            kembalian.setText(String.valueOf(Kembalian));  
            }    
        } catch (NumberFormatException e) {
             JOptionPane.showMessageDialog(this, "Masukkan jumlah yang valid", "PERINGATAN", JOptionPane.WARNING_MESSAGE, Peringatan);
            kembalian.setText("Masukkan angka");
        }
    }//GEN-LAST:event_cashKeyReleased

