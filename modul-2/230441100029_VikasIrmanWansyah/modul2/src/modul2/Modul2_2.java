    private void TotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TotalActionPerformed
        // TODO add your handling code here:
        int total1 = 0;
        int total = 0;
        int diskon = 0;
        int totalharga = 0;

        if(Menu1.isSelected()){
            total1 = jumlah * 45000 ;
        }else if(Menu2.isSelected()) {
            total1 = jumlah * 30000 ;
        }else if(Menu3.isSelected()) {
            total1 = jumlah * 40000 ;
        }else if(Menu4.isSelected()) {
            total1 = jumlah * 55000 ;
        }else if(Menu5.isSelected()) {
            total1 =  jumlah * 70000 ;
        }else if(Menu6.isSelected()) {
            total1 = jumlah * 60000 ;
        }else {
            JOptionPane.showMessageDialog(this,"Silahkan Pilih Salah Satu Menu","PERNGATAN",JOptionPane.WARNING_MESSAGE, Peringatan);
            return;
        }

        if(Topping1.isSelected()) {
            total = 0;
        }else if(Topping2.isSelected()) {
            total += 4000 * jumlah;
        }else if(Topping3.isSelected()) {
            total += 3000 * jumlah;
        }else if(Topping4.isSelected()) {
            total += 5000 * jumlah;
        }else{
            JOptionPane.showMessageDialog(this,"Silahkan Pilih Salah Satu Topping","PERNGATAN",JOptionPane.WARNING_MESSAGE, Peringatan);
            return;
        }
        totalharga = total1 + total ;
        if(totalharga > 100000) {
            diskon = totalharga * 5 / 100;
            totalharga -= diskon;
            totalHarga.setText(String.valueOf(totalharga));
        }else if (total1 == 0){
            totalHarga.setText("");
        }else{
            totalHarga.setText(String.valueOf(totalharga));
        }
        
        if (jumlah <= 0) {
            JOptionPane.showMessageDialog(this, "Masukkan jumlah yang valid", "PERINGATAN", JOptionPane.WARNING_MESSAGE, Peringatan);
            return;
}

    }//GEN-LAST:event_TotalActionPerformed

