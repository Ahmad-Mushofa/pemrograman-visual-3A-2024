    private void BtnTebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnTebakActionPerformed
        // TODO add your handling code here:
    try {
        int angka2 = Integer.parseInt(MasukanAngka.getText());

        // Cek apakah angka yang dimasukkan lebih dari 100 atau kurang dari 1
        if (angka2 > 100 || angka2 < 1) {
            JOptionPane.showMessageDialog(this, "Pilih Angka 1 - 100", "Peringatan", JOptionPane.WARNING_MESSAGE);
            MasukanAngka.setText(""); // Kosongkan text field
        } else {
            if (tebakan > angka2) {
                kesempatan -= 1;
                BtnTebak.setText("Tebak (" + kesempatan + ")");
                Hasil.setText("Angka Tebakan Lebih Kecil");
                MasukanAngka.setText("");
                Score.setText("0");
                JOptionPane.showMessageDialog(this, "TEBAKAN ANDA SALAH", "PESAN PEMBERITAHUAN", JOptionPane.INFORMATION_MESSAGE);

                if (kesempatan == 0) {
                    JOptionPane.showMessageDialog(this, "KESEMPATAN ANDA HABIS \nANDA GAGAL", "PESAN PERINGATAN", JOptionPane.ERROR_MESSAGE);
                    kesempatan = 10; // Reset kesempatan
                    BtnTebak.setText("Tebak (10)");
                }

            } else if (tebakan < angka2) {
                kesempatan -= 1;
                BtnTebak.setText("Tebak (" + kesempatan + ")");
                Hasil.setText("Angka Tebakan Lebih Besar");
                MasukanAngka.setText("");
                Score.setText("0");
                JOptionPane.showMessageDialog(this, "TEBAKAN ANDA SALAH", "PESAN PEMBERITAHUAN", JOptionPane.INFORMATION_MESSAGE);

                if (kesempatan == 0) {
                    JOptionPane.showMessageDialog(this, "KESEMPATAN ANDA HABIS \nANDA GAGAL", "PESAN PERINGATAN", JOptionPane.ERROR_MESSAGE);
                    kesempatan = 10; // Reset kesempatan
                    BtnTebak.setText("Tebak (10)");
                }

            } else {
                // Jika tebakan benar
                Hasil.setText(String.valueOf(tebakan));
                int hasil = kesempatan * 10; // Menghitung skor berdasarkan kesempatan
                Score.setText(String.valueOf(hasil));
                JOptionPane.showMessageDialog(this, "SELAMAT TEBAKAN ANDA BENAR", "INFORMASI", JOptionPane.INFORMATION_MESSAGE);

                // Mengambil input nama dari pemain
                String nama;
                boolean namaUnik;

                do {
                    nama = JOptionPane.showInputDialog(this, "SILAHKAN MASUKAN NAMA", "Nama player", JOptionPane.INFORMATION_MESSAGE);
                    namaUnik = true;

                    if (nama != null && !nama.trim().isEmpty()) {
                        // Cek apakah nama sudah terdaftar
                        for (int i = 0; i < index; i++) {
                            if (data_score[i][0].equals(nama)) {
                                JOptionPane.showMessageDialog(this, "Nama sudah terdaftar! Silakan masukkan nama lain.", "Peringatan", JOptionPane.WARNING_MESSAGE);
                                namaUnik = false;
                                break;
                            }
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Nama tidak boleh kosong!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                        namaUnik = false;
                    }
                } while (!namaUnik);

                // Simpan nama jika unik
                if (nama != null && namaUnik && index < data_score.length) {
                    data_score[index][0] = nama; // Menyimpan nama
                    data_score[index][1] = String.valueOf(hasil); // Menyimpan skor
                    index++; // Increment index setelah data ditambahkan
                }

                // Bersihkan inputan
                MasukanAngka.setText("");
                Hasil.setText("");
                Score.setText("0");
                kesempatan = 10; // Reset kesempatan
                BtnTebak.setText("Tebak (10)");

                // Bersihkan StringBuilder sebelum menambahkan data baru
                score_border.setLength(0);

                // Tambahkan data baru ke dalam score_border dengan nomor urut
                for (int i = 0; i < index; i++) {
                    score_border.append((i + 1)).append(". ") // Penomoran
                                .append("Name: ").append(data_score[i][0])
                                .append(", Score: ").append(data_score[i][1])
                                .append("\n");
                }

                // Set text area dengan score_border yang diperbarui
                hasilScore.setText(score_border.toString());
            }
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "TEBAKAN HARUS BERUPA ANGKA", "PESAN PERINGATAN", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_BtnTebakActionPerformed

