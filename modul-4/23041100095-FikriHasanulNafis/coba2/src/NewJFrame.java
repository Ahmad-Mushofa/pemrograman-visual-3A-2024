 private void resetGame() {
    target = new Random().nextInt(100) + 1; // Menghasilkan angka acak antara 1-100
    attempts = 0; // Mengatur ulang jumlah tebakan
    remainingAttempts = MAX_ATTEMPTS; // Mengatur ulang sisa tebakan
    tfcobatebak.setText(""); // Mengosongkan kolom input
    tfcek.setText(""); // Mengosongkan teks petunjuk
    tfscore.setText("Tebakan Tersisa: " + remainingAttempts); // Menampilkan sisa tebakan
}
private void handleGuess() {
    try {
        int guess = Integer.parseInt(tfcobatebak.getText());
        attempts++;
        remainingAttempts--; // Mengurangi jumlah kesempatan

        tfscore.setText("Tebakan Tersisa: " + remainingAttempts); // Menampilkan sisa tebakan

        if (guess < target) {
            tfcek.setText("Angka lebih besar."); // Memberi tahu pengguna
        } else if (guess > target) {
            tfcek.setText("Angka lebih kecil."); 
        } else {
            int score = (MAX_ATTEMPTS - attempts) * 10; // Menghitung score
            String name = JOptionPane.showInputDialog(this, "Anda Berhasil! Masukkan nama Anda:");
            if (name != null && !name.isEmpty()) {
                scoreHistory.add(name + ": " + score); // Menambahkan ke riwayat skor
                updateScoreArea(); // Update skor
            }
            resetGame(); // Mengatur ulang permainan
            return;
        }

        if (remainingAttempts <= 0) {
            JOptionPane.showMessageDialog(this, "Game Over! Angka yang benar adalah: " + target);
            resetGame(); 
        }

    } catch (NumberFormatException e) {
        tfcek.setText(""); 
    }
}
 
  private void updateScoreArea() { 
    StringBuilder scores = new StringBuilder();
    int index = 1; // Mulai penomoran dari 1
    for (String score : scoreHistory) {
        scores.append(index).append(". ").append(score).append("\n"); // Menambahkan nomor urut
        index++;
    }
  
    Scorearea.setText(scores.toString()); // Menggunakan JTextArea
}

