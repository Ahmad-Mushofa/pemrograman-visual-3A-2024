    private void tebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tebakActionPerformed
        // TODO add your handling code here:
        if (tebakan.getText().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Silahkan masukkan tebakan terlebih dahulu!",
                    "Peringatan",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
                return;
            }
        try {
            int tebakanUser = Integer.parseInt(tebakan.getText());
            
            if (tebakanUser < 1 || tebakanUser > 100) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Masukkan angka antara 1-100!",
                "Peringatan",
                javax.swing.JOptionPane.WARNING_MESSAGE);
            tebakan.setText("");
            return;
        }
            kesempatanTersisa--;
            sisa.setText(String.valueOf(kesempatanTersisa));
            if (tebakanUser == angkaRandom) {
                int skor = kesempatanTersisa * 10;
                String nama = javax.swing.JOptionPane.showInputDialog(this, 
                    "Selamat Tebakan Anda Benar " + angkaRandom + "\n Silahkan Masukkan nama Anda:");
                if (nama != null && !nama.trim().isEmpty()) {
                    jTextArea1.append(nama + " - Skor: " + skor + "\n");
                }
                mulaiGame();
            } else if (kesempatanTersisa == 0) {
                javax.swing.JOptionPane.showMessageDialog(this, 
                    "Anda Gagal! Angka yang benar adalah " + angkaRandom);
                mulaiGame();
            } else if (tebakanUser < angkaRandom) {
                hasil.setText("Tebakan terlalu kecil");
            } else {
                hasil.setText("Tebakan terlalu besar");
            }
            
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Masukkan angka yang valid (1-100)!");
                tebakan.setText("");
        }
    }//GEN-LAST:event_tebakActionPerformed

