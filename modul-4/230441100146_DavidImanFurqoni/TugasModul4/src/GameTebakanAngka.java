    private void mulaiGame() {
        angkaRandom = (int) (Math.random() * 100) + 1;
        kesempatanTersisa = 10;
        tebakan.setText("");
        hasil.setText("");
        sisa.setText(String.valueOf(kesempatanTersisa));
        tebakan.setEnabled(true);
        tebak.setEnabled(true);
    }
    
